import Foundation
import SourceryRuntime

// Swift code common to C# and dart
final class IotaTranslator: Translator {
    required init() {}

    func moduleRegisterTypesFn(context: FishyJoesContext) -> String {
        "FishyJoes_\(context.module.name.mangled)_registerTypes"
    }

    func translate(method: Method, context: FishyJoesContext) -> [SourceFragment] {
        let exportAnnotation = method.exportAnnotation

        var selfExpression: String
        let containingNamespace: String

        if let selfType = method.definedIn {
            let resolved = context.resolve(type: selfType)
            containingNamespace = resolved.sourceType.name

            if method.isStatic {
                selfExpression = containingNamespace
            } else {
                selfExpression = "\(resolved.converterType.name).peekIota(_iotaThis, env: env)"
            }
        } else {
            containingNamespace = context.module.name
            selfExpression = context.module.name
        }

        var formals = [(name: "envRef", type: "EnvRef")]
        if !method.isStatic {
            formals.append((name: "_iotaThis", type: "foreignObject"))
        }
        formals.append(
            contentsOf: method.parameters.map { parameter in
                let resolved = context.resolve(type: parameter.type, generics: exportAnnotation.genericOverrides)
                return (name: parameter.name, type: resolved.converterType.name + ".CType")
            }
        )
        formals.append((name: "_exn", type: "foreignOutExn"))

        let fragment = context.swiftFragment(
            "IotaInterface/\(containingNamespace)+iota-methods.swift",
            additionalImports: ["Foundation", "FishyJoesIotaRuntime"]
        )
        fragment.output("// Generated by FishyJoes for `\(containingNamespace).\(method.name)`")
        let returnType = context.resolve(type: method.returnType, generics: exportAnnotation.genericOverrides)
        let returnSignature = "\(returnType.converterType.name).CType"

        let cMethod = "__iota_\(containingNamespace)_\(exportAnnotation.name)".mangled
        if let deprecation = method.deprecation {
            fragment.output(deprecation.swiftDeprecation)
        }
        fragment.output("@_cdecl(\"\(cMethod)\")")
        fragment.outputBlock("public func \(cMethod)(", newLineTerminated: false) {
            fragment.outputMap(formals, separator: ",") { formal in
                "\(formal.name): \(formal.type)"
            }
        }
        fragment.outputBlock(" -> \(returnSignature) {") {
            fragment.output("let env = Env(envRef)")
            fragment.outputBlock("return env.catching(to: _exn) {") {
                let callName = method.sourceKind == .initializer ? "" : ".\(method.callName)"

                var mutateBlock: (() -> Void) -> Void = { $0() }
                if method.isMutating {
                    mutateBlock = { body in
                        fragment.outputBlock("return try \(containingNamespace).mutateIota(_iotaThis, env: env) { mutatingSelf in", closeWith: "}") {
                            body()
                        }
                    }
                    selfExpression = "mutatingSelf"
                }

                mutateBlock {
                    let body = {
                        fragment.outputBlock("\(selfExpression)\(callName)(", newLineTerminated: false) {
                            fragment.outputMap(method.parameters, separator: ",") { formal in
                                let resolved = context.resolve(type: formal.type, generics: exportAnnotation.genericOverrides)
                                return (formal.label.map { "\($0): " } ?? "") +
                                    "try \(resolved.converterType.name).peekIota(\(formal.name), env: env)"
                            }
                        }
                    }
                    if exportAnnotation.noReturn {
                        fragment.outputBlock("try (") {
                            body()
                            fragment.output()
                        }
                    } else {
                        fragment.outputBlock("return try \(returnType.converterType.name).toIota(") {
                            body()
                            fragment.output(",")
                            fragment.output("env: env")
                        }
                    }
                }
            }
        }

        return [fragment]
    }

    func translate(variable: Variable, context: FishyJoesContext) -> [SourceFragment] {
        guard let exportAnnotation = variable.exportAnnotation else {
            return []
        }
        let selfExpression: String
        let containingNamespace: String

        if let selfType = variable.definedInTypeName?.better {
            containingNamespace = context.resolve(type: selfType).sourceType.name

            if variable.isStatic {
                selfExpression = containingNamespace
            } else {
                selfExpression = "\(containingNamespace).peekIota(_iotaThis, env: env)"
            }
        } else {
            containingNamespace = context.module.name
            selfExpression = context.module.name
        }

        let iotaName = exportAnnotation.name
        let iotaGetName = "__iota_get_\(containingNamespace)_\(iotaName)".mangled
        let iotaSetName = "__iota_set_\(containingNamespace)_\(iotaName)".mangled

        let fragment = context.swiftFragment(
            "IotaInterface/\(containingNamespace)+iota-methods.swift",
            additionalImports: ["Foundation", "FishyJoesIotaRuntime"]
        )

        let resolved = context.resolve(type: variable.typeName.better)
        let converterName = resolved.converterType.name
        let cType = "\(converterName).CType"

        // Getter
        var formals = [(name: "envRef", type: "EnvRef")]
        if !variable.isStatic {
            formals.append((name: "_iotaThis", type: "foreignObject"))
        }
        formals.append((name: "_exn", type: "foreignOutExn"))

        fragment.output("// Generated by FishyJoes for `\(containingNamespace).\(variable.name)`")
        if let deprecation = variable.deprecation {
            fragment.output(deprecation.swiftDeprecation)
        }
        fragment.output("@_cdecl(\"\(iotaGetName)\")")
        fragment.outputBlock("public func \(iotaGetName)(", newLineTerminated: false) {
            fragment.outputMap(formals, separator: ",") { formal in
                "\(formal.name): \(formal.type)"
            }
        }
        fragment.outputBlock(" -> \(cType) {") {
            fragment.output("let env = Env(envRef)")
            fragment.outputBlock("return env.catching(to: _exn) {") {
                fragment.output("try \(converterName).toIota(\(selfExpression).\(variable.name), env: env)")
            }
        }

        // Setter
        if variable.isPubliclyWritable {
            var formals = [(name: "envRef", type: "EnvRef")]
            if !variable.isStatic {
                formals.append((name: "_iotaThis", type: "foreignObject"))
            }
            formals.append((name: "newValue", type: cType))
            formals.append((name: "_exn", type: "foreignOutExn"))

            if let deprecation = variable.deprecation {
                fragment.output(deprecation.swiftDeprecation)
            }
            fragment.output("@_cdecl(\"\(iotaSetName)\")")
            fragment.outputBlock("public func \(iotaSetName)(", newLineTerminated: false) {
                fragment.outputMap(formals, separator: ",") { formal in
                    "\(formal.name): \(formal.type)"
                }
            }
            fragment.outputBlock(" {") {
                fragment.output("let env = Env(envRef)")
                fragment.outputBlock("env.catching(to: _exn) {") {
                    if variable.isStatic {
                        fragment.output("\(selfExpression).\(variable.name) = try \(converterName).peekIota(newValue, env: env)")
                    } else {
                        fragment.outputBlock("try \(containingNamespace).mutateIota(_iotaThis, env: env) { value in", closeWith: "}") {
                            fragment.output("value.\(variable.name) = try \(converterName).peekIota(newValue, env: env)")
                        }
                    }
                }
            }
        }

        return [fragment]
    }

    func setupFragments(context: FishyJoesContext, generatedTypes: [BetterType]) -> [SourceFragment] {
        let swiftSetupFragment = context.swiftFragment(
            "IotaInterface/TypeSetup.swift",
            additionalImports: ["Foundation", "FishyJoesIotaRuntime"]
        )

        swiftSetupFragment.output("@_cdecl(\"\(moduleRegisterTypesFn(context: context))\")")
        swiftSetupFragment.outputBlock("public func \(moduleRegisterTypesFn(context: context))() {") {
            for type in generatedTypes {
                let resolved = context.resolve(type: type)
                swiftSetupFragment.output("Env.registerType(\(resolved.converterType.name).self, as: \"\(resolved.converterType.name)\")")
            }
        }

        let exportFragment = SourceFragment(sourceryDestination: "file:IotaInterface/@_exported.swift")
        exportFragment.output("@_exported import \(context.module.name)")
        for dependency in context.module.dependencies {
            exportFragment.output("@_exported import \(dependency)_IotaInterface")
        }

        return [swiftSetupFragment, exportFragment]
    }
}
