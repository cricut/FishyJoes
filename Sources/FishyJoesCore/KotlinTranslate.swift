import Foundation
import SourceryRuntime

class KotlinTranslate {
    func javaClassName(_ name: String, in context: FishyJoesContext) -> String {
        "com/cricut/\(context.module.lowercased())/\(name.replacingOccurrences(of: ".", with: "$"))"
    }

    var allMethods: [String: [(javaName: String, signature: String, cName: String)]] = [:]

    func translate(method: Method, context: FishyJoesContext) -> [SourceFragment] {
        let exportAnnotation = method.exportAnnotation

        var selfExpression: String
        let containingNamespace: String

        if let selfType = method.definedIn {
            let resolved = context.resolve(type: selfType)
            containingNamespace = resolved.converterType.name

            if method.isStatic {
                selfExpression = containingNamespace
            } else {
                selfExpression = "\(resolved.converterType.name).fromJava(_javaThis, env: _javaEnv)"
            }
        } else {
            containingNamespace = context.module
            selfExpression = context.module
        }

        let formals = [
            (name: "_javaEnv", type: "UnsafeMutablePointer<JNIEnv?>"),
            (name: "_javaThis", type: "jobject"),
        ] + method.parameters.map { parameter in
            let resolved = context.resolve(type: parameter.type, generics: exportAnnotation.genericOverrides)
            return (name: parameter.name, type: resolved.converterType.name + ".CType")
        }
        let fragment = context.swiftFragment(
            "JavaInterface/\(containingNamespace)+javadecl.swift",
            additionalImports: ["Foundation", "FishyJoesJavaRuntime", "CommonInterface"]
        )
        fragment.output("// Generated by FishyJoes for `\(containingNamespace).\(method.name)`")
        var jniSignature = ""
        for parameter in method.parameters {
            let resolved = context.resolve(type: parameter.type, generics: exportAnnotation.genericOverrides)
            jniSignature += resolved.jniType.asSignature
        }
        let returnType = context.resolve(type: method.returnType, generics: exportAnnotation.genericOverrides)
        let returnSignature = "\(returnType.converterType.name).CType"
        jniSignature = "(\(jniSignature))\(returnType.jniType.asSignature)"

        let cMethod = "java_\(containingNamespace)_\(exportAnnotation.name)".replacingOccurrences(of: ".", with: "_")
        allMethods[containingNamespace, default: []].append(("__jni_\(exportAnnotation.name)", jniSignature, cMethod))
        fragment.outputBlock("let \(cMethod): @convention(c) (", newLineTerminated: false) {
            fragment.outputMap(formals, separator: ",", \.type)
        }
        fragment.outputBlock(" -> \(returnSignature) = { \(formals.map(\.name).joined(separator: ", ")) in", closeWith: "}") {
            fragment.outputBlock("FishyJoesJavaRuntime.callbackBody(_javaEnv) { _javaEnv in", closeWith: "}") {
                let callName = method.isInitializer ? "" : ".\(method.callName)"

                // let isMutating = method.isMutating || method.modifiers.contains(where: { $0.name == "mutating" })
                var mutateBlock: (() -> Void) -> Void = { $0() }
                if method.isMutating {
                    fragment.output("var mutatingSelf = try \(selfExpression)")
                    mutateBlock = { body in
                        fragment.outputBlock("return try \(containingNamespace).mutateJava(_javaThis, env: _javaEnv) { mutatingSelf in", closeWith: "}") {
                            body()
                        }
                    }
                    selfExpression = "mutatingSelf"
                }

                mutateBlock {
                    fragment.outputBlock("return try \(returnType.converterType.name).toJava(") {
                        fragment.outputBlock("\(selfExpression)\(callName)(", closeWith: "),") {
                            fragment.outputMap(method.parameters, separator: ",") { formal in
                                let resolved = context.resolve(type: formal.type, generics: exportAnnotation.genericOverrides)
                                return (formal.label.map { "\($0): " } ?? "") +
                                    "try \(resolved.converterType.name).fromJava(\(formal.name), env: _javaEnv)"
                            }
                        }
                        fragment.output("env: _javaEnv")
                    }
                }
            }
        }

        return [fragment]
    }

    func translate(variable: Variable, context: FishyJoesContext) -> [SourceFragment] {
        guard let exportAnnotation = variable.exportAnnotation else {
            return []
        }
        let kotlinName = exportAnnotation.name
        let jvmGetName = "__jni_\(exportAnnotation.kind == .asMethod ? "" : "get_")\(kotlinName)"
        let jvmSetName = "__jni_set_\(kotlinName)"

        var selfExpression: String
        let containingNamespace: String

        if let selfType = variable.definedInTypeName?.better {
            let resolved = context.resolve(type: selfType)
            containingNamespace = resolved.converterType.name

            if variable.isStatic {
                selfExpression = containingNamespace
            } else {
                selfExpression = "\(resolved.converterType.name).fromJava(_javaThis, env: _javaEnv)"
            }
        } else {
            containingNamespace = context.module
            selfExpression = context.module
        }

        let cGetName = "java_get_\(containingNamespace)_\(exportAnnotation.name)".replacingOccurrences(of: ".", with: "_")
        let cSetName = "java_set_\(containingNamespace)_\(exportAnnotation.name)".replacingOccurrences(of: ".", with: "_")

        let fragment = context.swiftFragment(
            "JavaInterface/\(containingNamespace)+javadecl.swift",
            additionalImports: ["Foundation", "FishyJoesJavaRuntime", "CommonInterface"]
        )

        let resolved = context.resolve(type: variable.typeName.better)
        let jniSignature = resolved.jniType.asSignature
        let converterName = resolved.converterType.name
        let cType = "\(converterName).CType"

        // Getter
        let formals = [
            (name: "_javaEnv", type: "UnsafeMutablePointer<JNIEnv?>"),
            (name: "_javaThis", type: "jobject"),
        ]

        allMethods[containingNamespace, default: []].append((jvmGetName, "()" + jniSignature, cGetName))
        fragment.output("// Generated by FishyJoes for `\(containingNamespace).\(variable.name)`")
        fragment.outputBlock("let \(cGetName): @convention(c) (", newLineTerminated: false) {
            fragment.outputMap(formals, separator: ",", \.type)
        }
        fragment.outputBlock(" -> \(cType) = { \(formals.map(\.name).joined(separator: ", ")) in", closeWith: "}") {
            fragment.outputBlock("FishyJoesJavaRuntime.callbackBody(_javaEnv) { _javaEnv in", closeWith: "}") {
                fragment.output("try \(converterName).toJava(\(selfExpression).\(variable.name), env: _javaEnv)")
            }
        }

        // Setter
        if variable.isMutable, variable.accessLevel.write == .public {
            let formals = [
                (name: "_javaEnv", type: "UnsafeMutablePointer<JNIEnv?>"),
                (name: "_javaThis", type: "jobject"),
                (name: "newValue", type: cType),
            ]
            allMethods[containingNamespace, default: []].append((jvmSetName, "(\(jniSignature))V", cSetName))
            fragment.outputBlock("let \(cSetName): @convention(c) (", newLineTerminated: false) {
                fragment.outputMap(formals, separator: ",", \.type)
            }
            fragment.outputBlock(" -> Void = { \(formals.map(\.name).joined(separator: ", ")) in", closeWith: "}") {
                fragment.outputBlock("FishyJoesJavaRuntime.callbackBody(_javaEnv) { _javaEnv in", closeWith: "}") {
                    if variable.isStatic {
                        fragment.output("\(selfExpression).\(variable.name) = try \(converterName).fromJava(newValue, env: _javaEnv)")
                    } else {
                        fragment.outputBlock("try \(containingNamespace).mutateJava(_javaThis, env: _javaEnv) { value in", closeWith: "}") {
                            fragment.output("value.\(variable.name) = try \(converterName).fromJava(newValue, env: _javaEnv)")
                        }
                    }
                }
            }
        }

        return [fragment]
    }

    func setupFragment(context: FishyJoesContext, generatedTypes: Set<BetterType>) -> SourceFragment {
        let javaTypeListFragment = context.swiftFragment(
            "JavaInterface/TypeSetup.swift",
            additionalImports: ["Foundation", "FishyJoesJavaRuntime", "CommonInterface"]
        )

        var usedMethods: Set<String> = []

        javaTypeListFragment.output("@_cdecl(\"JNI_OnLoad\")")
        javaTypeListFragment.outputBlock("public func JNIOnLoad(vm: UnsafeMutablePointer<JavaVM?>, reserved: UnsafeMutableRawPointer) -> jint {") {
            javaTypeListFragment.outputBlock("guard let env = try? JVM(javaVM: vm).currentThreadEnv() else {") {
                javaTypeListFragment.output("fatalError(\"Couldn't obtain jvm environment\")")
            }
            javaTypeListFragment.outputBlock("return env.callbackBody {") {
                javaTypeListFragment.output("let bag = CStringBag()")
                for type in generatedTypes.sorted(by: { "\($0)" < "\($1)" }) {
                    guard type != .void else { continue }
                    let resolved = context.resolve(type: type)
                    javaTypeListFragment.output("//print(\"setting up \(resolved.converterType.name)...\")")
                    javaTypeListFragment.output("try \(resolved.converterType.name).javaSetup(env: env)")
                    if case .named = type {
                        if let nativeMethods = allMethods[resolved.converterType.name] {
                            usedMethods.insert(resolved.converterType.name)
                            javaTypeListFragment.outputBlock("try env.RegisterNatives(", closeWith: ")") {
                                javaTypeListFragment.output("\(resolved.converterType.name).externalWitnessClass ?? \(resolved.converterType.name).javaClass", newLineTerminated: false)
                                for (method, signature, cName) in nativeMethods {
                                    javaTypeListFragment.output(",")
                                    javaTypeListFragment.outputBlock("JNINativeMethod(", newLineTerminated: false) {
                                        javaTypeListFragment.output("name: bag.add(\"\(method)\"),")
                                        javaTypeListFragment.output("signature: bag.add(\"\(signature)\"),")
                                        javaTypeListFragment.output("fnPtr: unsafeBitCast(\(cName), to: UnsafeMutableRawPointer.self)")
                                    }
                                }
                                javaTypeListFragment.output()
                            }
                        }
                    }
                }
                javaTypeListFragment.output("return JNI_VERSION_1_4")
            }
        }

        let droppedMethods = Set(allMethods.keys).subtracting(usedMethods)
        guard droppedMethods.isEmpty else {
            fatalErr("methods for \(droppedMethods) were never set up! Probably a bug.")
        }

        return javaTypeListFragment
    }

    func kotlin(method: Method, context: FishyJoesContext) -> KotlinClass.MethodOrVariable? {
        let exportAnnotation = method.exportAnnotation
        var omitParameters = Set(exportAnnotation.omitParameters)
        var parameters: [(labelComment: String?, name: String, KotlinClass.KType)] = []
        for parameter in method.parameters {
            if omitParameters.contains(parameter.name) {
                precondition(parameter.defaultValue != nil, "Can't omit non-default parameter")
                omitParameters.remove(parameter.name)
                continue
            }
            let resolved = context.resolve(type: parameter.type, generics: exportAnnotation.genericOverrides)
            var label: String?
            if let swiftLabel = parameter.label, swiftLabel != parameter.name {
                label = swiftLabel
            }
            parameters.append((label, parameter.name, resolved.kotlinType))
        }

        return .method(
            KotlinClass.Method(
                documentation: method.documentation,
                isStatic: method.isStatic,
                isOverride: method.exportAnnotation.isOverride,
                name: exportAnnotation.name,
                parameters: parameters,
                returnType: context.resolve(type: method.returnType, generics: exportAnnotation.genericOverrides).kotlinType,
                body: nil
            )
        )
    }

    func kotlin(field: Variable, context: FishyJoesContext, useNativeName: Bool = false) -> KotlinClass.MethodOrVariable? {
        let ktName: String
        var asMethod = false

        if useNativeName {
            guard field.exportAnnotation == nil else {
                fatalErr("field \(field.name) should not be annotated, as it's in a type being exported memberwise")
            }
            ktName = field.name
        } else {
            guard let exportAnnotation = field.exportAnnotation else {
                return nil
            }
            asMethod = exportAnnotation.kind == .asMethod
            ktName = exportAnnotation.name
        }
        let resolved = context.resolve(type: field.typeName.better)
        if asMethod {
            return .method(
                KotlinClass.Method(
                    documentation: field.documentation,
                    isStatic: field.isStatic,
                    isOverride: field.exportAnnotation?.isOverride ?? false,
                    name: ktName,
                    parameters: [],
                    returnType: resolved.kotlinType,
                    body: nil
                )
            )
        } else {
            return .variable(
                KotlinClass.Variable(
                    documentation: field.documentation,
                    isStatic: field.isStatic,
                    isOverride: field.exportAnnotation?.isOverride ?? false,
                    readOnly: !field.isMutable,
                    name: ktName,
                    type: resolved.kotlinType
                )
            )
        }
    }

    func conform(_ kotlinClass: KotlinClass, to protoName: String, context: FishyJoesContext) {
        let resolved = context.resolve(type: .named(.init(name: protoName)))

        kotlinClass.conformances.append(resolved.kotlinName)

        if let proto = resolved as? TranslatedProtocol {
            let protoDefs = Set(
                proto.methods.compactMap { context.kotlin(method: $0).map(OverrideSignature.init) } +
                    proto.computedVariables.compactMap { context.kotlin(field: $0).map(OverrideSignature.init) }
            )

            kotlinClass.methods = kotlinClass.methods.map { method in
                var modified = method
                if protoDefs.contains(OverrideSignature(.method(method))) {
                    modified.isOverride = true
                }
                return modified
            }
            kotlinClass.fields = kotlinClass.fields.map { field in
                var modified = field
                if protoDefs.contains(OverrideSignature(.variable(field))) {
                    modified.isOverride = true
                }
                return modified
            }
            if let product = kotlinClass as? KotlinProductClass {
                product.constructor.fields = product.constructor.fields.map { field in
                    var modified = field
                    if protoDefs.contains(OverrideSignature(.variable(field))) {
                        modified.isOverride = true
                    }
                    return modified
                }
            }
        }
    }
}

extension KotlinClass {
    func conforming(to protocols: Set<String>, context: FishyJoesContext) -> KotlinClass {
        protocols.reduce(into: self) { kClass, proto in
            context.kotlinTranslator.conform(kClass, to: proto, context: context)
        }
    }
}

fileprivate struct OverrideSignature: Hashable {
    let isStatic: Bool
    let name: String
    let parameters: [KotlinClass.KType]?
    let returnType: KotlinClass.KType

    init(_ methodOrVar: KotlinClass.MethodOrVariable) {
        switch methodOrVar {
        case .method(let method):
            isStatic = method.isStatic
            name = method.name
            parameters = method.parameters.map(\.2)
            returnType = method.returnType
        case .variable(let variable):
            isStatic = variable.isStatic
            name = variable.name
            parameters = nil
            returnType = variable.type
        }
    }
}
