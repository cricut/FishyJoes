package com.cricut.testapi

import org.junit.jupiter.api.Assertions.*
import org.junit.jupiter.api.Test

internal class PrimitiveTests {
    @Test
    fun testExtrema() {
        assertEquals(Primitives.falseBool, false)
        assertEquals(Primitives.trueBool, true)
        assertEquals(Primitives.zeroUInt8, 0x00u.toUByte())
        assertEquals(Primitives.minUInt8, 0x00u.toUByte())
        assertEquals(Primitives.maxUInt8, 0xFFu.toUByte())
        assertEquals(Primitives.zeroUInt16, 0x0000u.toUShort())
        assertEquals(Primitives.minUInt16, 0x0000u.toUShort())
        assertEquals(Primitives.maxUInt16, 0xFFFFu.toUShort())
        assertEquals(Primitives.zeroUInt32, 0x00000000u.toUInt())
        assertEquals(Primitives.minUInt32, 0x00000000u.toUInt())
        assertEquals(Primitives.maxUInt32, 0xFFFFFFFFu.toUInt())
//        assertEquals(Primitives.zeroUInt64, 0x0000000000000000u.toULong())
//        assertEquals(Primitives.minUInt64, 0x0000000000000000u.toULong())
//        assertEquals(Primitives.maxUInt64, 0xFFFFFFFFFFFFFFFFu.toULong())
        assertEquals(Primitives.zeroInt8, 0x00.toByte())
        assertEquals(Primitives.minInt8, (-0x80).toByte())
        assertEquals(Primitives.maxInt8, (+0x7F).toByte())
        assertEquals(Primitives.zeroInt16, 0x0000.toShort())
        assertEquals(Primitives.minInt16, (-0x8000).toShort())
        assertEquals(Primitives.maxInt16, (+0x7FFF).toShort())
        assertEquals(Primitives.zeroInt32, 0x00000000.toInt())
        assertEquals(Primitives.minInt32, (-0x80000000).toInt())
        assertEquals(Primitives.maxInt32, (+0x7FFFFFFF).toInt())
//        assertEquals(Primitives.zeroInt64, 0x0000000000000000.toLong())
//        assertEquals(Primitives.minInt64, (-0x8000000000000000).toLong())
//        assertEquals(Primitives.maxInt64, (+0x7FFFFFFFFFFFFFFF).toLong())
        assertEquals(Primitives.zeroFloat, 0.0f)
        assertEquals(Primitives.minFloat, -Float.MAX_VALUE)
        assertEquals(Primitives.maxFloat, Float.MAX_VALUE)
        assertEquals(Primitives.zeroDouble, 0.0)
        assertEquals(Primitives.minDouble, -Double.MAX_VALUE)
        assertEquals(Primitives.maxDouble, Double.MAX_VALUE)
    }

    @Test
    fun testEcho() {
        assertEquals(Primitives.echoBool(false), false)
        assertEquals(Primitives.echoBool(true), true)
        assertEquals(Primitives.echoUInt8(0x00u.toUByte()), 0x00u.toUByte())
        assertEquals(Primitives.echoUInt8(0x00u.toUByte()), 0x00u.toUByte())
        assertEquals(Primitives.echoUInt8(0xFFu.toUByte()), 0xFFu.toUByte())
        assertEquals(Primitives.echoUInt16(0x0000u.toUShort()), 0x0000u.toUShort())
        assertEquals(Primitives.echoUInt16(0x0000u.toUShort()), 0x0000u.toUShort())
        assertEquals(Primitives.echoUInt16(0xFFFFu.toUShort()), 0xFFFFu.toUShort())
        assertEquals(Primitives.echoUInt32(0x00000000u.toUInt()), 0x00000000u.toUInt())
        assertEquals(Primitives.echoUInt32(0x00000000u.toUInt()), 0x00000000u.toUInt())
        assertEquals(Primitives.echoUInt32(0xFFFFFFFFu.toUInt()), 0xFFFFFFFFu.toUInt())
//        assertEquals(Primitives.echoUInt64(0x0000000000000000u.toULong()), 0x0000000000000000u.toULong())
//        assertEquals(Primitives.echoUInt64(0x0000000000000000u.toULong()), 0x0000000000000000u.toULong())
//        assertEquals(Primitives.echoUInt64(0xFFFFFFFFFFFFFFFFu.toULong()), 0xFFFFFFFFFFFFFFFFu.toULong())
        assertEquals(Primitives.echoInt8(0x00u.toByte()), 0x00.toByte())
        assertEquals(Primitives.echoInt8((-0x80).toByte()), (-0x80).toByte())
        assertEquals(Primitives.echoInt8((+0x7F).toByte()), (+0x7F).toByte())
        assertEquals(Primitives.echoInt16(0x0000.toShort()), 0x0000.toShort())
        assertEquals(Primitives.echoInt16((-0x8000).toShort()), (-0x8000).toShort())
        assertEquals(Primitives.echoInt16((+0x7FFF).toShort()), (+0x7FFF).toShort())
        assertEquals(Primitives.echoInt32(0x00000000.toInt()), 0x00000000.toInt())
        assertEquals(Primitives.echoInt32((-0x80000000).toInt()), (-0x80000000).toInt())
        assertEquals(Primitives.echoInt32((+0x7FFFFFFF).toInt()), (+0x7FFFFFFF).toInt())
//        assertEquals(Primitives.echoInt64(0x0000000000000000.toLong()), 0x0000000000000000.toLong())
//        assertEquals(Primitives.echoInt64((-0x8000000000000000).toLong()), (-0x8000000000000000).toLong())
//        assertEquals(Primitives.echoInt64((+0x7FFFFFFFFFFFFFFF).toLong()), (+0x7FFFFFFFFFFFFFFF).toLong())
        assertEquals(Primitives.echoFloat(0.0f), 0.0f)
        assertEquals(Primitives.echoFloat(-Float.MAX_VALUE), -Float.MAX_VALUE)
        assertEquals(Primitives.echoFloat(Float.MAX_VALUE), Float.MAX_VALUE)
        assertEquals(Primitives.echoDouble(0.0), 0.0)
        assertEquals(Primitives.echoDouble(-Double.MAX_VALUE), -Double.MAX_VALUE)
        assertEquals(Primitives.echoDouble(Double.MAX_VALUE), Double.MAX_VALUE)
    }

